# Five basic math operations

# STEP 1 - input the numbers
# If you use int with input, it treats your input as numbers!
print("Please enter two numbers: ")
num1 = int(input("First number? "))
num2 = int(input("Second number? "))

# STEP 2 - do your calculations! (we needed the numbers first!)
total = num1 + num2
diff = num1 - num2
prod = num1 * num2
quot = num1 / num2
power = num1 ** num2

#STEP 3 - show you answers
print("The total (+) is", total)
print("The difference (-) is", diff)
print("The product (*) is", prod)
print("The quotient (/) is", quot)
print("The power (^) is", power)
